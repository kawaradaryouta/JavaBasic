public class PTra02_01 {
	public static void main(String[] args) {

		// ★ 20を5で割った値を出力してください
		int a=20;
		a/=5;
		System.out.println(a);
		// ★ 126に13を掛けた値を出力してください
		int b=126;
		b*=13;
		System.out.println(b);
		// ★ 97を6で割ったときの余りを出力してください

		int c=97;
		c%=6;
		System.out.println(c);
	}
}
